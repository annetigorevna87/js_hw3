var mark = 5;

switch (mark) {
	case 5:
		console.log('Поздравляю! Вы успешно сдали экзамен на отлично!');
		break;
	case 4:
		console.log('Вы молодец! Хорошо справились с экзаменом!');
		break;
	case 3:
		console.log('Неплохо! Но в следующий раз готовьтесь к экзамену лучше!');
		break;
	case 2:
		console.log('К сожалению, Вам придется прийти на пересдачу!');
		break;
	default:
		console.log('Такой оценки не существует!');	
}